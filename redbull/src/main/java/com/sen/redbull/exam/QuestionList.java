package com.sen.redbull.exam;

public class QuestionList {
	private String id;
	private String options;
	private String question;
	private float score;
	private String successanswer;
	private String testquestype;
	private String type;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOptions() {
		return options;
	}
	public void setOptions(String options) {
		this.options = options;
	}
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
	public float getScore() {
		return score;
	}
	public void setScore(float score) {
		this.score = score;
	}
	public String getSuccessanswer() {
		return successanswer;
	}
	public void setSuccessanswer(String successanswer) {
		this.successanswer = successanswer;
	}
	public String getTestquestype() {
		return testquestype;
	}
	public void setTestquestype(String testquestype) {
		this.testquestype = testquestype;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

	
	
}
